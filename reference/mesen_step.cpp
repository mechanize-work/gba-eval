// Mesen2 frame-step shim. Implements the GBA emulator wasm ABI defined
// in ../spec/ABI.md.
//
// We use the WHOLE Emulator (not just GbaConsole) — Mesen's GBA core is
// welded to it via _emu->ProcessMemoryRead<>() on every memory access.
// The MESEN_HEADLESS patches make Emulator single-threaded and GBA-only:
// no thread launches, sync video decode, no frame limiter.
//
// Framebuffer path: GbaPpu writes RGB555 to _currentBuffer →
// SendFrame() → VideoDecoder::UpdateFrame(sync=true) → DecodeFrame() →
// GbaDefaultVideoFilter expands to RGB888 → VideoRenderer::UpdateFrame()
// stores it. We grab the post-filter buffer.

