// ───────────────────────────────────────────────────────────────────────────
// Native: turn the shim into a standalone audio dump tool. Wasm builds
// skip this — the host calls the exports directly.
// ───────────────────────────────────────────────────────────────────────────

static void write_le16(FILE* f, uint16_t v) { fwrite(&v, 2, 1, f); }
static void write_le32(FILE* f, uint32_t v) { fwrite(&v, 4, 1, f); }

int main(int argc, char** argv) {
    if (argc != 4) {
        fprintf(stderr, "usage: %s <rom.gba> <frames> <out.wav>\n", argv[0]);
        return 1;
    }
    int frames = atoi(argv[2]);

    if (!mesen_init()) { fprintf(stderr, "init failed\n"); return 1; }

    FILE* rf = fopen(argv[1], "rb");
    if (!rf) { perror(argv[1]); return 1; }
    int len = (int)fread(mesen_rom_buffer(), 1, ROM_MAX, rf);
    fclose(rf);
    if (!mesen_load_rom(len)) { fprintf(stderr, "load_rom failed\n"); return 1; }

    std::vector<int16_t> samples;
    samples.reserve(frames * 1200);
    int first_audio = -1;
    // Track the rate as observed once audio is actually flowing — games set
    // SOUNDBIAS during init, so the silent prefix may be at default 32768
    // and the body at 65536. Picking it after first nonzero sample gets the
    // operating rate. (If a game CHANGES rate mid-stream the WAV is wrong
    // either way; the warning prints and the comparison script will see a
    // tempo discontinuity. None of the test ROMs do this.)
    uint32_t rate = 32768;

    for (int f = 0; f < frames; ++f) {
        mesen_run_frame();
        int n = mesen_audio_samples();
        int16_t* buf = mesen_audio_buffer();
        if (first_audio < 0) {
            for (int i = 0; i < n * 2; ++i) {
                if (buf[i] != 0) { first_audio = f; break; }
            }
        }
        if (first_audio >= 0) {
            uint32_t r = mesen_audio_rate();
            if (rate != r && rate != 32768) {
                fprintf(stderr, "WARNING: sample rate changed %u -> %u at frame %d\n", rate, r, f);
            }
            rate = r;
        }
        samples.insert(samples.end(), buf, buf + n * 2);
    }

    // The BIOS stub stashes unhandled SWI numbers at IWRAM 0x7FF4. If a
    // game called something we don't implement, the music engine probably
    // broke silently — surface that here, not in the WAV stats. Common
    // misses are LZ77UnComp (0x11/0x12) and SoundDriverMain (0x1C-0x1F),
    // the latter being how some games delegate the entire m4a step to BIOS.
    {
        uint8_t unk = mesen_peek_iwram(0x7FF4);
        if (unk) {
            fprintf(stderr, "WARNING: BIOS stub hit unhandled SWI 0x%02X (last seen)\n", unk);
        }
    }

    // MESEN_DUMP_IWRAM=N → re-run from cold and snapshot IWRAM at
    // frame N. Useful for diffing an emulator's IWRAM state against
    // Mesen's at a known frame offset.
    if (const char* fenv = getenv("MESEN_DUMP_IWRAM")) {
        int target = atoi(fenv);
        mesen_reset();
        for (int f = 0; f <= target; ++f) mesen_run_frame();
        auto mem = g_emu->GetMemory(MemoryType::GbaIntWorkRam);
        if (mem.Memory && mem.Size >= 0x8000) {
            std::string p = std::string(argv[3]) + ".iwram.f" + std::to_string(target);
            FILE* iw = fopen(p.c_str(), "wb");
            if (iw) {
                fwrite(mem.Memory, 1, 0x8000, iw);
                fclose(iw);
                fprintf(stderr, "dumped IWRAM at frame %d to %s\n", target, p.c_str());
            }
        }
    }

    // WAV header — rate from SOUNDBIAS, not assumed 32768.
    FILE* out = fopen(argv[3], "wb");
    if (!out) { perror(argv[3]); return 1; }
    uint32_t data_bytes = samples.size() * 2;
    fwrite("RIFF", 1, 4, out); write_le32(out, 36 + data_bytes); fwrite("WAVE", 1, 4, out);
    fwrite("fmt ", 1, 4, out); write_le32(out, 16);
    write_le16(out, 1); write_le16(out, 2);
    write_le32(out, rate); write_le32(out, rate * 4);
    write_le16(out, 4); write_le16(out, 16);
    fwrite("data", 1, 4, out); write_le32(out, data_bytes);
    fwrite(samples.data(), 2, samples.size(), out);
    fclose(out);

    fprintf(stderr, "ran %d frames -> %zu stereo samples (%.2fs at %u Hz)\n",
            frames, samples.size()/2, (double)(samples.size()/2)/rate, rate);
    if (first_audio >= 0) fprintf(stderr, "first audio at frame %d\n", first_audio);
    else fprintf(stderr, "WARNING: all samples are zero\n");
    fprintf(stderr, "wrote %s\n", argv[3]);
    return 0;
}
