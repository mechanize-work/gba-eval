#define GBA_W 240
#define GBA_H 160
#define ROM_MAX (32 * 1024 * 1024)

// ───────────────────────────────────────────────────────────────────────────
// Static state — single instance, same as the other shims.
// ───────────────────────────────────────────────────────────────────────────

static uint32_t g_framebuffer[GBA_W * GBA_H];
static uint8_t  g_rom[ROM_MAX];
static size_t   g_rom_len = 0;

static std::unique_ptr<Emulator> g_emu;
static uint32_t g_keys = 0;

// IInputProvider runs after ClearState/SetStateFromInput inside
// BaseControlManager::UpdateInputState, so this is where we inject keys.
//
// GbaController::Buttons is { Up=0, Down, Left, Right, Start, Select, B, A, L, R }.
// GBA KEYINPUT (our ABI) is   { A=0,  B,    Select, Start, Right, Left, Up, Down, R, L }.
// Orders are unrelated; map explicitly.
struct KeyProvider : IInputProvider {
    bool SetInput(BaseControlDevice* dev) override {
        if (dev->GetControllerType() != ControllerType::GbaController) return false;
        // Our bit → GbaController::Buttons enum value:
        static const int map[10] = {
            GbaController::A, GbaController::B, GbaController::Select, GbaController::Start,
            GbaController::Right, GbaController::Left, GbaController::Up, GbaController::Down,
            GbaController::R, GbaController::L,
        };
        dev->ClearState();
        for (int i = 0; i < 10; i++) {
            if ((g_keys >> i) & 1) dev->SetBit(map[i]);
        }
        return true;  // we handled it; stop the chain
    }
};
static KeyProvider g_key_provider;

// ───────────────────────────────────────────────────────────────────────────
// Framebuffer extraction. The video filter writes 0xAARRGGBB (Mesen's
// internal format, see GbaDefaultVideoFilter::ApplyFilter). The ABI
// wants 0xAABBGGRR, so we swap R and B per pixel.
// ───────────────────────────────────────────────────────────────────────────

static void capture_frame() {
    // After UpdateFrame(sync=true) → DecodeFrame() → filter ran.
    // Emulator::GetVideoFilter() allocates a NEW filter every call (its
    // _outputBuffer is null until the first SendFrame, which never comes).
    // VideoDecoder::_videoFilter is the one that ACTUALLY decoded —
    // GetFilter() is our fork's accessor.
    BaseVideoFilter* filter = g_emu->GetVideoDecoder()->GetFilter();
    if (!filter) return;

    uint32_t* src = filter->GetOutputBuffer();
    if (!src) return;

    // ARGB → ABGR (the ABI's 0xAABBGGRR).
    for (int i = 0; i < GBA_W * GBA_H; i++) {
        uint32_t p = src[i];
        g_framebuffer[i] = (p & 0xFF00FF00u) | ((p & 0xFF) << 16) | ((p >> 16) & 0xFF);
    }
}

